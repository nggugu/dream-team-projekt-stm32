void gpio_pump_state(uint8_t state){

	GPIO_PinState pinState;

	pinState = (state == 1)? GPIO_PIN_SET : GPIO_PIN_RESET;

	HAL_GPIO_WritePin(GPIOG, PUMP_PinNumber , pinState);

}

